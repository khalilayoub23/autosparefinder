UPSERT_SQL = """
INSERT INTO parts_catalog
    (id, sku, name, category, manufacturer, part_type,
     description, specifications, compatible_vehicles,
     base_price, is_active, created_at, updated_at)
VALUES ($1, $2, $3, $4, $5, $6, $7, $8::jsonb, $9::jsonb, $10, true, NOW(), NOW())
ON CONFLICT (sku) DO UPDATE SET
    name = EXCLUDED.name,
    category = EXCLUDED.category,
    manufacturer = EXCLUDED.manufacturer,
    part_type = EXCLUDED.part_type,
    description = EXCLUDED.description,
    specifications = EXCLUDED.specifications,
    compatible_vehicles = EXCLUDED.compatible_vehicles,
    base_price = EXCLUDED.base_price,
    is_active = true,
    updated_at = NOW()
"""


async def populate():
    conn = await asyncpg.connect(DB_URL)
    print(f"Connected — importing {len(PARTS)} parts for {len({p[0] for p in PARTS})} manufacturers...\n")

    batch = []
    counts: dict[str, int] = {}

    for brand, catalog_num, name_he, category, part_type, price in PARTS:
        sku = f"{brand[:6].upper().replace(' ', '_')}-{str(catalog_num).replace(' ', '')}"[:100]
        specs = json.dumps({"manufacturer_name": brand}, ensure_ascii=False)
        compat = json.dumps([{"make": brand}], ensure_ascii=False)
        batch.append((
            uuid.uuid4(), sku, name_he, category, brand,
            part_type, name_he, specs, compat, float(price)
        ))
        counts[brand] = counts.get(brand, 0) + 1

    # Upsert all at once, fallback to row-by-row on error
    try:
        await conn.executemany(UPSERT_SQL, batch)
        total = len(batch)
    except Exception:
        total = 0
        for rec in batch:
            try:
                await conn.execute(UPSERT_SQL, *rec)
                total += 1
            except Exception as e:
                print(f"  ✗ skipped {rec[1]}: {e}")

    print("Results by manufacturer:")
    for brand, cnt in sorted(counts.items()):
        print(f"  {brand:<20} {cnt:>4} parts")

    db_total = await conn.fetchval("SELECT COUNT(*) FROM parts_catalog")
    await conn.close()

    print(f"\n{'='*50}")
    print(f"Upserted : {total} parts")
    print(f"DB total : {db_total:,}")
    print(f"{'='*50}")


if __name__ == "__main__":
    asyncio.run(populate())
